    private void jTextArea1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextArea1KeyReleased
       //system.out.println("нажата клавиша"+evt.getKeyChar());
       sb.setCaret(String.valueOf(evt.getKeyChar()));
       int posCaret = jTextArea1.getCaretPosition();
       try {
           //возвращает номер текущей строки в jTextArea
           int line = jTextArea1.getLineOfOffset(jTextArea1.getCaretPosition());
           //возвращает номер символа в строке относительно начало строки
           int column = posCaret - jTextArea1.getLineStartOffset(line);
           
           sb.setCaret((line + 1) +" : "+(column+1));
       } catch (BadLocationException ex) {
            Logger.getLogger(MainPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jTextArea1KeyReleased

